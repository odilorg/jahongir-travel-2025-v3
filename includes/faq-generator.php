    /**
     * Generate FAQ HTML with Schema markup
     */
    public static function generateFAQHTML($faqs, $title = 'Frequently Asked Questions') {
        if (empty($faqs)) {
            return '';
        }
        
        $html = '<div class="faq-section" style="margin: 40px 0; padding: 30px; background: #f8f9fa; border-radius: 10px;">';
        $html .= '<h2 style="color: #333; margin-bottom: 30px; text-align: center; font-size: 2em;">' . $title . '</h2>';
        $html .= '<div class="faq-container" style="max-width: 800px; margin: 0 auto;">';
        
        foreach ($faqs as $index => $faq) {
            $html .= '<div class="faq-item" style="margin-bottom: 20px; background: white; border-radius: 8px; overflow: hidden; box-shadow: 0 2px 5px rgba(0,0,0,0.1);">';
            $html .= '<div class="faq-question" style="padding: 20px; background: #007cba; color: white; cursor: pointer; font-weight: bold; font-size: 1.1em;" onclick="toggleFAQ(' . $index . ')">';
            $html .= $faq['question'];
            $html .= '<span class="faq-toggle" style="float: right; font-size: 1.2em;">+</span>';
            $html .= '</div>';
            $html .= '<div class="faq-answer" id="faq-answer-' . $index . '" style="padding: 20px; display: none; line-height: 1.6; color: #555;">';
            $html .= $faq['answer'];
            $html .= '</div>';
            $html .= '</div>';
        }
        
        $html .= '</div>';
        $html .= '</div>';
        
        // Add JavaScript for FAQ functionality
        $html .= '<script>
        function toggleFAQ(index) {
            const answer = document.getElementById("faq-answer-" + index);
            const toggle = document.querySelector(".faq-item:nth-child(" + (index + 1) + ") .faq-toggle");
            
            if (answer.style.display === "none") {
                answer.style.display = "block";
                toggle.textContent = "âˆ’";
            } else {
                answer.style.display = "none";
                toggle.textContent = "+";
            }
        }
        </script>';
        
        return $html;
    }
    
    /**
     * Generate FAQ Schema markup
     */
    public static function generateFAQSchema($faqs) {
        $schemaItems = [];
        
        foreach ($faqs as $faq) {
            $schemaItems[] = [
                "@type" => "Question",
                "name" => $faq['question'],
                "acceptedAnswer" => [
                    "@type" => "Answer",
                    "text" => $faq['answer']
                ]
            ];
        }
        
        $schema = [
            "@context" => "https://schema.org",
            "@type" => "FAQPage",
            "mainEntity" => $schemaItems
        ];
        
        return '<script type="application/ld+json">' . json_encode($schema, JSON_PRETTY_PRINT | JSON_UNESCAPED_SLASHES) . '</script>';
    }
    
