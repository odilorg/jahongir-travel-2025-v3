										<h2>Traditional Main Dishes</h2>
										
										<h3>1. Samarkand Plov (Osh)</h3>
										<p>Plov is Uzbekistan's national dish, and Samarkand has its own unique version. Our local plov is characterized by:</p>
										<ul>
											<li><strong>Golden Rice:</strong> Perfectly cooked basmati rice with saffron</li>
											<li><strong>Tender Meat:</strong> Usually lamb or beef, slow-cooked to perfection</li>
											<li><strong>Sweet Carrots:</strong> Orange carrots add natural sweetness</li>
											<li><strong>Garlic:</strong> Whole garlic cloves cooked with the rice</li>
											<li><strong>Raisins:</strong> Sweet raisins for a unique flavor contrast</li>
										</ul>
										<p><strong>Best Places:</strong> Plov Center near Registan Square, local family restaurants in the old city</p>
										
										<h3>2. Lagman (Noodle Soup)</h3>
										<p>This hearty noodle soup is perfect for Samarkand's cooler months:</p>
										<ul>
											<li><strong>Hand-Pulled Noodles:</strong> Fresh noodles made daily</li>
											<li><strong>Rich Broth:</strong> Meat-based broth with vegetables</li>
											<li><strong>Fresh Vegetables:</strong> Bell peppers, tomatoes, onions</li>
											<li><strong>Spices:</strong> Traditional Uzbek spices and herbs</li>
										</ul>
										<p><strong>Best Places:</strong> Small family restaurants in the old city, especially near Siab Bazaar</p>
										
										<h3>3. Manti (Steamed Dumplings)</h3>
										<p>These steamed dumplings are a favorite among locals:</p>
										<ul>
											<li><strong>Thin Dough:</strong> Delicate wrapper made from scratch</li>
											<li><strong>Meat Filling:</strong> Ground lamb or beef with onions</li>
											<li><strong>Steamed to Perfection:</strong> Cooked in traditional steamers</li>
											<li><strong>Sour Cream:</strong> Served with fresh sour cream</li>
										</ul>
										
										<h2>Bread & Pastries</h2>
										
										<h3>4. Non (Traditional Bread)</h3>
										<p>Samarkand is famous for its traditional bread, baked in clay ovens:</p>
										<ul>
											<li><strong>Clay Oven Baking:</strong> Traditional tandoor-style ovens</li>
											<li><strong>Unique Patterns:</strong> Decorative patterns pressed into the dough</li>
											<li><strong>Crispy Crust:</strong> Golden, crispy exterior</li>
											<li><strong>Soft Interior:</strong> Fluffy, warm bread inside</li>
											<li><strong>Long Shelf Life:</strong> Can last for weeks</li>
										</ul>
										<p><strong>Best Places:</strong> Local bakeries near Registan Square, family-run bakeries in residential areas</p>
										
										<h3>5. Samsa (Meat Pastries)</h3>
										<p>These triangular pastries are perfect for a quick meal:</p>
										<ul>
											<li><strong>Flaky Pastry:</strong> Crispy, layered dough</li>
											<li><strong>Meat Filling:</strong> Lamb or beef with onions</li>
											<li><strong>Clay Oven Baking:</strong> Baked in traditional ovens</li>
											<li><strong>Hot & Fresh:</strong> Best eaten immediately</li>
										</ul>
										
										<h2>Street Food & Snacks</h2>
										
										<h3>6. Shashlik (Grilled Meat)</h3>
										<p>Grilled meat skewers are popular throughout Uzbekistan:</p>
										<ul>
											<li><strong>Fresh Meat:</strong> Usually lamb or beef</li>
											<li><strong>Marinated:</strong> Traditional spices and herbs</li>
											<li><strong>Grilled to Order:</strong> Cooked over charcoal</li>
											<li><strong>Fresh Bread:</strong> Served with warm non bread</li>
										</ul>
										
										<h3>7. Chuchvara (Dumplings)</h3>
										<p>Small dumplings in broth, similar to wontons:</p>
										<ul>
											<li><strong>Small Size:</strong> Bite-sized dumplings</li>
											<li><strong>Meat Filling:</strong> Ground meat with spices</li>
											<li><strong>Broth:</strong> Clear, flavorful broth</li>
											<li><strong>Fresh Herbs:</strong> Garnished with dill and parsley</li>
										</ul>
										
										<h2>Sweet Treats & Desserts</h2>
										
										<h3>8. Halva</h3>
										<p>Traditional sweet confection made from sesame seeds:</p>
										<ul>
											<li><strong>Sesame Base:</strong> Ground sesame seeds</li>
											<li><strong>Honey or Sugar:</strong> Natural sweeteners</li>
											<li><strong>Nuts:</strong> Often includes pistachios or almonds</li>
											<li><strong>Rich Flavor:</strong> Dense, sweet, and satisfying</li>
										</ul>
										
										<h3>9. Navat (Rock Sugar)</h3>
										<p>Traditional Uzbek candy made from sugar:</p>
										<ul>
											<li><strong>Pure Sugar:</strong> Crystallized sugar</li>
											<li><strong>Natural Colors:</strong> Sometimes colored with saffron</li>
											<li><strong>Sweet Treat:</strong> Perfect with tea</li>
											<li><strong>Long Shelf Life:</strong> Can be stored for months</li>
										</ul>
										
										<h3>10. Fresh Fruits</h3>
										<p>Samarkand is famous for its fresh fruits:</p>
										<ul>
											<li><strong>Pomegranates:</strong> Sweet, juicy pomegranates</li>
											<li><strong>Grapes:</strong> Various varieties of table grapes</li>
											<li><strong>Melons:</strong> Sweet, aromatic melons</li>
											<li><strong>Apricots:</strong> Fresh apricots in season</li>
										</ul>
										
										<h2>Where to Eat in Samarkand</h2>
										
										<h3>Traditional Restaurants</h3>
										<ul>
											<li><strong>Plov Center:</strong> Best place for authentic plov</li>
											<li><strong>Old City Restaurants:</strong> Family-run establishments</li>
											<li><strong>Bazaar Food Stalls:</strong> Fresh, local street food</li>
											<li><strong>Hotel Restaurants:</strong> Upscale dining with traditional dishes</li>
										</ul>
										
										<h3>Local Markets</h3>
										<ul>
											<li><strong>Siab Bazaar:</strong> Fresh produce and local specialties</li>
											<li><strong>Registan Area:</strong> Tourist-friendly food stalls</li>
											<li><strong>Residential Areas:</strong> Authentic local bakeries</li>
										</ul>
										
										<h2>Dining Etiquette & Tips</h2>
										
										<h3>Traditional Customs</h3>
										<ul>
											<li><strong>Remove Shoes:</strong> In traditional restaurants and homes</li>
											<li><strong>Wash Hands:</strong> Before eating, especially with bread</li>
											<li><strong>Share Food:</strong> Meals are often communal</li>
											<li><strong>Respect Bread:</strong> Never waste bread - it's sacred</li>
										</ul>
										
										<h3>Ordering Tips</h3>
										<ul>
											<li><strong>Ask Locals:</strong> Get recommendations from residents</li>
											<li><strong>Try Everything:</strong> Sample different dishes</li>
											<li><strong>Fresh is Best:</strong> Order dishes made to order</li>
											<li><strong>Portion Sizes:</strong> Uzbek portions are generous</li>
										</ul>
										
