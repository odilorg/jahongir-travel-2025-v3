												<h2><strong>Day 3 Tashkent- Urgench - Khiva</strong></h2>
												<p>Breakfast. Transfer to the local airport and take a flight to Urgench. Transfer to Khiva (30 km). Installation for one night in a hotel located in Ichan - Kala (the interior and ancient part of the city). Guided tour of the city. Khiva is one of the remarkable cities of Khorezm Khanat (khanat - a big Empire).</p>
												<p> Being the capital of the Khanat in XVII century, it was one of the largest markets of Central Asia and its prosperity lasted until the beginning of the XX century. Today, the interior city Ichan Kala is a true open museum where one can admire the authenticity of the monuments Kounia-Ark (the residence of Khans) XVI century, the mausoleum of Pahlavan Mahmoud XV century, the Friday mosque, the madrasah of Khoja Islam XIX century, etc. The city is surrounded by pink clay fortifications. Overnight in Khiva.</p>
