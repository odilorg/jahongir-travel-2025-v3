											<p><span class="icon-left">4</span></p>
											<div class="item_content">
												<h2><strong>Day 4 Khiva –Bukhara</strong></h2>
												<p>Breakfast. You take a long drive (480 km, 9-10 hours) through the Kyzyl Kum desert along the banks of the Amudarya River to Bukhara. The rest of the day is devoted to checking in the hotel and leisure. The construction of the new road make the course little bit demanding and it takes about 9- 10 hours. Arrival in Bukhara in the late afternoon and accommodation at the hotel. Overnight in Bukhara</p>
											</div>
										</div>
										<div class="interary-item">
											<p><span class="icon-left">5</span></p>
											<div class="item_content">
												<h2><strong>Day 5 Bukhara</strong></h2>
												<p>Breakfast. Bukhara is a real Oriental fairytale, city with small romantic streets, unsurpassed sights that preserved the beauty and charm of the ancient walls. Monuments are located close to each other and that’s why sightseeing tour will be organized almost without use of vehicle. In this day you will see: Samanids’ dynasty mausoleum (IX-X cc), Chashma-Ayub burial-vault (XIV-XIX cc), Minorai Kalon ensemble, Madrassah Miri-Arab, Madrassah Ulugbek and Abdulazizkhan, Labi Hauz complex (XVI-XVII), Kukeldash Madrassah (1568), Arc Fortress (VI-VIIcc), Chor-Minor Madrassah, Visit will continue to the trade domes of ancient Bukhara bazaar - even if you won’t buy any souvenir or adornment, you will get many pleasant impressions about Orient and its inhabitants. Overnight in Bukhara.</p>
											</div>
										</div>
										<div class="interary-item">
											<p><span class="icon-left">6</span></p>
											<div class="item_content">
												<h2><strong>Day 6 Bukhara – Nurata – Yurt Camp</strong></h2>
												<p>Breakfast at the hotel. Transfer to the real yurt camp near the lake Aydarkul. Visit Nurata, where you will see: “Chashma” holy spring - Namozgokh Mosque (10 c) which was reconstructed by Emir Abdullah in 1582 - Sheykh Khasan Nuri Madrassah (16-17 cc) - ruins of Alexander the Great citadel. Check in the yurts. Dinner under the open sky sparkling with millions of stars. After dinner you will see fascinating folklore show: “Akyn” – singer of the national tunes will be singing for you. Overnight in yurts.</p>
											</div>
										</div>
